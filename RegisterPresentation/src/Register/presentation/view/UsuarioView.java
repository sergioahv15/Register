    public void update(java.util.Observable updatedModel,Object parametros){

           if (parametros != UsuarioModel.USUARIO_CURRENT) return;

           Usuario usuarioCurrent = model.getUsuarioCurrent();

           this.cedulaFld.setEnabled(model.getModo()==Application.MODO_AGREGAR);

           Integer cedula=usuarioCurrent.getCedula();
           if(cedula==0)cedulaFld.setText("");
           else cedulaFld.setText(cedula.toString());
           
           passFld.setText(usuarioCurrent.getClave());
           
           tipoFld.setSelectedItem(usuarioCurrent.getTipo());

            this.validate();
            if (!model.getMensaje().equals("")){
                JOptionPane.showMessageDialog(this, model.getMensaje(), "",JOptionPane.INFORMATION_MESSAGE);
            }
        }
    
    
